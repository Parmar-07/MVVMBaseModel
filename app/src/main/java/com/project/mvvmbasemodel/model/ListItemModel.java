package com.project.mvvmbasemodel.model;

public class ListItemModel {

    private String item1;
    private String item2;

    public ListItemModel(String item1, String item2) {
        this.item1 = item1;
        this.item2 = item2;
    }

    public String getItem1() {
        return item1;
    }


    public String getItem2() {
        return item2;
    }

}
